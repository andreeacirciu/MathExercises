package org.example;

public class Division {
    public double division(double a, double b) {
        double division = a / b;
        System.out.println(division);
        return division;
    }

    public int division(int a, int b) {
        int division = a / b;
        System.out.println(division);
        return division;
    }

    public float division(float a, float b) {
        float division = a/b;
        System.out.println(division);
        return division;
    }
}
