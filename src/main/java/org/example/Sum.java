package org.example;

public class Sum {
    public double sum(double a, double b) {
        double sum = a + b;
        System.out.println(sum);
        return sum;
    }

    public int sum(int a, int b) {
        int sum = a + b;
        System.out.println(sum);
        return sum;
    }

    public float sum(float a, float b) {
        float sum = a + b;
        System.out.println(sum);
        return sum;
    }
}
